        Confessions du meilleur buteur de l'équipe de France de football.
        L'édition poche mise à jour avec son parcours en Ligue des champions et à l'Euro 2021.

        " Je me suis construit dans l'adversité. J'ai passé ma carrière à prouver. Que je méritais ma place. Que j'étais légitime à mon poste ou dans une équipe. J'ai parfois eu le sentiment que la vie était injuste, mais je n'ai jamais renoncé. "

        La vie d'Olivier Giroud n'est pas un long fleuve tranquille. Deuxième meilleur buteur de l'Équipe de France, derrière Thierry Henry, l'attaquant est tour à tour porté aux nues puis critiqué par la presse. Passionné de ballon rond depuis petit, le gamin de Froges, prêt à tous les sacrifices, s'est acharné à croire en ses ambitions. Et il a réussi. Son éducation fondée sur le travail, le respect et l'humilité, sa force de caractère et sa foi inébranlable en Jésus l'ont mené au sommet. À trente-quatre ans, il a toujours faim de victoires et de titres.

        Habituellement si discret, Olivier Giroud se confie sans fard : son enfance, l'argent, la notoriété, les coups durs, sa famille, sa rivalité supposée avec Benzema, les attaques dont il est la cible...

        Voici l'histoire d'un joueur que rien ne prédestinait à une carrière hors-normes, mais qui, à force de détermination, a réalisé ses rêves les plus fous.
